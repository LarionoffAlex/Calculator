public class Addition {

    public static int doOperation(int num1I, int num2I) {
        return num1I + num2I;
    }

    public static double doOperation(double num1D, double num2D) {
        return num1D + num2D;
    }

    public static String doOperation(String num1S, String num2S) {
        return num1S + num2S;
    }
}
